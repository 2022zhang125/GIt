package com.hbsfdxwlxy.junit.service;

/**
 * 数学服务类
 * @author BelieveSun
 * @version 1.0
 * @since 1.0
 */
public class MathService {
    /**
     * 加法
     * @param a
     * @param b
     * @return
     */
    public int sum(int a, int b){
        return a + b;
    }

    /**
     * 减法
     * @param a
     * @param b
     * @return
     */
    public int sub(int a, int b){
        return a - b;
    }
}
